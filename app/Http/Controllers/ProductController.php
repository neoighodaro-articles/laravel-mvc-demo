     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $product = new Product([
            'name' => $request->name,
            'description' => $request->description,
            'price' => $request->price,
            'count' => $request->count
            ]);

        $product->save();

        $products = Product::all();

        return view('viewproducts', ['products' => $products]);
    }

